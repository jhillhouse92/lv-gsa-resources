package com.longview.gsa.domain;

public class OpenFDA {

	String[] application_number;
	String[] brand_name;
	String[] dosage_form;
	String[] generic_name;
	String[] manufacture_name;
	String[] substance_name;
	String[] pharm_class_epc;
	
	public String[] getApplication_number() {
		return application_number;
	}
	public void setApplication_number(String[] application_number) {
		this.application_number = application_number;
	}
	public String[] getBrand_name() {
		return brand_name;
	}
	public void setBrand_name(String[] brand_name) {
		this.brand_name = brand_name;
	}
	public String[] getDosage_form() {
		return dosage_form;
	}
	public void setDosage_form(String[] dosage_form) {
		this.dosage_form = dosage_form;
	}
	public String[] getGeneric_name() {
		return generic_name;
	}
	public void setGeneric_name(String[] generic_name) {
		this.generic_name = generic_name;
	}
	public String[] getManufacture_name() {
		return manufacture_name;
	}
	public void setManufacture_name(String[] manufacture_name) {
		this.manufacture_name = manufacture_name;
	}
	public String[] getSubstance_name() {
		return substance_name;
	}
	public void setSubstance_name(String[] substance_name) {
		this.substance_name = substance_name;
	}
	public String[] getPharm_class_epc() {
		return pharm_class_epc;
	}
	public void setPharm_class_epc(String[] pharm_class_epc) {
		this.pharm_class_epc = pharm_class_epc;
	}
	
	
}
